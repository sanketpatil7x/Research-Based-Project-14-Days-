def large_function_0(data):
    result = []
    temp = 0
    for i in range(len(data)):
        if isinstance(data[i], int):
            if data[i] > 0:
                temp += data[i]
            else:
                temp -= data[i]
        elif isinstance(data[i], str):
            if data[i].isdigit():
                temp += int(data[i])
            else:
                temp += len(data[i])
        else:
            temp += 1
        if temp % 2 == 0:
            result.append(temp * 2)
        else:
            result.append(temp + 3)
    print('Debug:', temp)
    return result

